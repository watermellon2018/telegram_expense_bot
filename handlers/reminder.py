def end_of_month_stats(update: Update, context: CallbackContext) -> None:
    """
    –û—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –ø–æ–ª–Ω—É—é —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –∑–∞ –º–µ—Å—è—Ü –ø–æ –∑–∞–ø—Ä–æ—Å—É
    –ò–º–∏—Ç–∏—Ä—É–µ—Ç —Ñ—É–Ω–∫—Ü–∏—é, –∫–æ—Ç–æ—Ä–∞—è –¥–æ–ª–∂–Ω–∞ –±—ã–ª–∞ –±—ã –≤—ã–ø–æ–ª–Ω—è—Ç—å—Å—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –≤ –∫–æ–Ω—Ü–µ –º–µ—Å—è—Ü–∞
    """
    user_id = update.effective_user.id
    
    # –ü–æ–ª—É—á–∞–µ–º —Ç–µ–∫—É—â–∏–π –º–µ—Å—è—Ü –∏ –≥–æ–¥
    now = datetime.datetime.now()
    month = now.month
    year = now.year
    month_name = helpers.get_month_name(month)
    
    # –ü–æ–ª—É—á–∞–µ–º —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É —Ä–∞—Å—Ö–æ–¥–æ–≤
    expenses = excel.get_month_expenses(user_id, month, year)
    
    # –§–æ—Ä–º–∏—Ä—É–µ–º –æ—Ç—á–µ—Ç
    report = (
        f"üìä –ò—Ç–æ–≥–æ–≤–∞—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ —Ä–∞—Å—Ö–æ–¥–æ–≤ –∑–∞ {month_name} {year} –≥–æ–¥–∞\n\n"
    )
    
    if not expenses or expenses['total'] == 0:
        report += f"–í {month_name} {year} –≥–æ–¥–∞ —Ä–∞—Å—Ö–æ–¥–æ–≤ –Ω–µ –±—ã–ª–æ."
        update.message.reply_text(report)
        return
    
    # –î–æ–±–∞–≤–ª—è–µ–º –æ–±—â—É—é —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É
    report += f"üí∞ –û–±—â–∞—è —Å—É–º–º–∞ —Ä–∞—Å—Ö–æ–¥–æ–≤: {expenses['total']:.2f}\n"
    report += f"üßæ –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ç—Ä–∞–Ω–∑–∞–∫—Ü–∏–π: {expenses['count']}\n\n"
    
    # –î–æ–±–∞–≤–ª—è–µ–º —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º
    report += "üìã –†–∞—Å—Ö–æ–¥—ã –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º:\n"
    
    # –°–æ—Ä—Ç–∏—Ä—É–µ–º –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –ø–æ —É–±—ã–≤–∞–Ω–∏—é —Å—É–º–º
    sorted_categories = sorted(
        expenses['by_category'].items(), 
        key=lambda x: x[1], 
        reverse=True
    )
    
    for category, amount in sorted_categories:
        from config import DEFAULT_CATEGORIES
        emoji = DEFAULT_CATEGORIES.get(category, "")
        percentage = (amount / expenses['total']) * 100
        report += f"{emoji} {category}: {amount:.2f} ({percentage:.1f}%)\n"
    
    # –î–æ–±–∞–≤–ª—è–µ–º —Å—Ç–∞—Ç—É—Å –±—é–¥–∂–µ—Ç–∞
    budget_status = helpers.format_budget_status(user_id, month, year)
    report += f"\n{budget_status}"
    
    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –æ—Ç—á–µ—Ç
    update.message.reply_text(report)
    
    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –∫—Ä—É–≥–æ–≤—É—é –¥–∏–∞–≥—Ä–∞–º–º—É
    chart_path = visualization.create_monthly_pie_chart(user_id, month, year)
    if chart_path and os.path.exists(chart_path):
        with open(chart_path, 'rb') as photo:
            update.message.reply_photo(photo=photo, caption="–†–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ä–∞—Å—Ö–æ–¥–æ–≤ –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º")
    
    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å—Ä–∞–≤–Ω–µ–Ω–∏–µ —Å –ø—Ä–µ–¥—ã–¥—É—â–∏–º –º–µ—Å—è—Ü–µ–º, –µ—Å–ª–∏ –≤–æ–∑–º–æ–∂–Ω–æ
    prev_month = month - 1
    prev_year = year
    if prev_month == 0:
        prev_month = 12
        prev_year = year - 1
    
    prev_expenses = excel.get_month_expenses(user_id, prev_month, prev_year)
    if prev_expenses and prev_expenses['total'] > 0:
        prev_month_name = helpers.get_month_name(prev_month)
        diff = expenses['total'] - prev_expenses['total']
        diff_percentage = (diff / prev_expenses['total']) * 100 if prev_expenses['total'] > 0 else 0
        
        comparison = f"üìà –°—Ä–∞–≤–Ω–µ–Ω–∏–µ —Å –ø—Ä–µ–¥—ã–¥—É—â–∏–º –º–µ—Å—è—Ü–µ–º:\n\n"
        comparison += f"{prev_month_name}: {prev_expenses['total']:.2f}\n"
        comparison += f"{month_name}: {expenses['total']:.2f}\n\n"
        
        if diff > 0:
            comparison += f"–£–≤–µ–ª–∏—á–µ–Ω–∏–µ —Ä–∞—Å—Ö–æ–¥–æ–≤: +{diff:.2f} (+{diff_percentage:.1f}%)"
        else:
            comparison += f"–£–º–µ–Ω—å—à–µ–Ω–∏–µ —Ä–∞—Å—Ö–æ–¥–æ–≤: {diff:.2f} ({diff_percentage:.1f}%)"
        
        update.message.reply_text(comparison)

